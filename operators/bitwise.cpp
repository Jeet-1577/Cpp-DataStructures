#include<iostream>
using namespace std;

int main(){
    int a = 5; // Example input, you can change this value
    int b = 6; // Example input, you can change this value


    cout << "a&b"  <<(a&b) << endl; // Bitwise AND
    cout << "a|b"  <<(a|b) << endl; // Bitwise OR
    cout<< "a^b"  <<(a^b) << endl; // Bitwise XOR
    cout << "~a"  <<(~a) << endl; // Bitwise NOT
}