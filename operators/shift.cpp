#include<iostream>
using namespace std;

int main(){

    cout<< (17>>1) << endl; // Right shift by 1 (divides by 2)
    cout<< (17>>2 )<< endl; // Right shift by 2 (divides by 4)
    cout<< (17<<1 )<< endl; // Left shift by 1 (multiplies by 2)
    cout<< (17<<2 )<< endl; // Left shift by 2 (multiplies by 4)
}